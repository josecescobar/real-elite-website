// Generates src/lib/projects/registry.generated.ts by scanning the project
// data directory, so adding a project is drop-a-file: create
// src/lib/projects/data/<slug>.ts (default-exporting a Project) and run
// `npm run generate:projects` (also runs automatically in `prebuild`).
//
// A staleness test in src/lib/projects/projects.test.ts fails CI if the
// registry is out of sync with the data directory.

import { readdirSync, writeFileSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const projectsDir = join(here, '..', 'src', 'lib', 'projects');
const dataDir = join(projectsDir, 'data');
const outFile = join(projectsDir, 'registry.generated.ts');

const slugs = readdirSync(dataDir)
  .filter((f) => f.endsWith('.ts') && !f.endsWith('.test.ts'))
  .map((f) => f.replace(/\.ts$/, ''))
  .sort();

const imports = slugs
  .map((slug, i) => `import project${i} from './data/${slug}';`)
  .join('\n');
const list = slugs.map((_, i) => `  project${i},`).join('\n');

const contents = `// AUTO-GENERATED by scripts/generate-projects-registry.mjs — do not edit by hand.
// Run \`npm run generate:projects\` after adding or removing a project in ./data.
import type { Project } from './types';
${imports}

/** Every authored project (published or draft). Order here does not matter. */
export const PROJECT_MODULES: Project[] = [
${list}
];
`;

// Only write when changed, to avoid needless churn / rebuilds.
let current = '';
try {
  current = readFileSync(outFile, 'utf8');
} catch {
  // file does not exist yet
}
if (current !== contents) {
  writeFileSync(outFile, contents);
  console.log(`[projects] wrote registry with ${slugs.length} project(s)`);
} else {
  console.log(`[projects] registry already up to date (${slugs.length} project(s))`);
}
